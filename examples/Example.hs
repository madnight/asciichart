import Data.Text.Chart (plot, plotWith, options, height)

main :: IO ()
main = do

  plot [0..14]
  --  14.00 ┼             ╭
  --  13.00 ┤            ╭╯
  --  12.00 ┤           ╭╯
  --  11.00 ┤          ╭╯
  --  10.00 ┤         ╭╯
  --   9.00 ┤        ╭╯
  --   8.00 ┤       ╭╯
  --   7.00 ┤      ╭╯
  --   6.00 ┤     ╭╯
  --   5.00 ┤    ╭╯
  --   4.00 ┤   ╭╯
  --   3.00 ┤  ╭╯
  --   2.00 ┤ ╭╯
  --   1.00 ┤╭╯
  --   0.00 ┼╯


  -- sinus example with options
  let wave = round . (10 *) . sin . (/ 120) . (pi *) . (4 *) <$> [0..60]
  plotWith options {height = 10} wave
  --  10.00 ┤           ╭──────╮
  --   8.00 ┤      ╭────╯      ╰────╮
  --   6.00 ┤     ╭╯                ╰╮
  --   4.00 ┤  ╭──╯                  ╰──╮
  --   2.00 ┤ ╭╯                        ╰╮
  --   0.00 ┼─╯                          ╰──╮                          ╭─
  --  -2.00 ┤                               ╰╮                        ╭╯
  --  -4.00 ┤                                ╰──╮                  ╭──╯
  --  -6.00 ┤                                   ╰╮                ╭╯
  --  -8.00 ┤                                    ╰────╮      ╭────╯
  -- -10.00 ┤                                         ╰──────╯
